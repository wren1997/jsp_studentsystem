package entities;

public class Lab {
    private int lab_id;
    private String lab_name;
    private String lab_manager;

    public int getLab_id() {
        return lab_id;
    }

    public void setLab_id(int lab_id) {
        this.lab_id = lab_id;
    }

    public String getLab_name() {
        return lab_name;
    }

    public void setLab_name(String lab_name) {
        this.lab_name = lab_name;
    }

    public String getLab_manager() {
        return lab_manager;
    }

    public void setLab_manager(String lab_manager) {
        this.lab_manager = lab_manager;
    }
}
